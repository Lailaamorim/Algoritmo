# FOR
# O for é uma estrutura de repetição usada quando sabemos
# quantas vezes o código deve repetir.

# Ele percorre uma sequência de valores.

# range() cria uma sequência de números.

# Estrutura básica
# for variável in range(inicio, fim):

# Exemplo:
for numero in range(1, 6):
    print(numero)

# O programa vai repetir mostrando:
# 1
# 2
# 3
# 4
# 5

# O 6 não aparece porque o range para um número antes.