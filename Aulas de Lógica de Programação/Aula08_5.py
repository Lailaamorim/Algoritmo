# 🔁 Exemplo 5 — percorrendo um vetor (muito importante)
# Isso é uma das coisas mais importantes de vetores

# Vetor com números
numeros = [5, 10, 15, 20] 

# Percorrendo o vetor usando o laço de repetição for 
for numero in numeros:
    print(numero)

# O for é usado para percorrer (passar por) todos os elementos de uma lista
# Ele pega um valor de cada vez automaticamente, sem precisar usar índices

# Estrutura:
# for variavel in lista:

# A cada repetição, a variável recebe um item da lista
# Ex: numero vai receber 5, depois 10, depois 15...

# Isso é muito importante porque evita código repetitivo
# e funciona mesmo se a lista tiver muitos valores

# Muito usado para:
# - mostrar dados na tela
# - somar valores (ex: calcular média)
# - fazer verificações (ex: números maiores que 10)
# - modificar dados
