# Contar de 10 até 0

contador = 10  # começa o contador em 10

# enquanto o contador for maior ou igual a 0
while contador >= 0:
    print(contador) # mostra o valor atual do contador
    contador = contador -1  # diminui 1 do contador a cada repetição
    
print("TERMINEI DE CONTAR")


'''
WHILE
O while é um laço de repetição.
Ele repete um bloco de código enquanto uma condição for verdadeira.

Verdadeiro (True):
quando a condição ainda está sendo satisfeita.
Exemplo: 3 < 5 → isso é verdadeiro.

Falso (False):
quando a condição não é mais satisfeita.
Exemplo: 6 < 5 → isso é falso.

Quando a condição fica falsa, o while para de repetir.
'''
# Exemplo:
contador = 1

while contador <= 3:  # enquanto essa condição for verdadeira
    print(contador)
    contador = contador + 1

# quando contador virar 4, a condição fica falsa e o while termina



